# print hello 5 times using while loop
count = 1;

while count <= 5 :
    print ("Hello", count)
    count = count + 1


# print 1 to 5
num = 1
while num <= 5 :
    print (num)
    num = num + 1


# print 5 to 1
num = 5
while num >= 1 :
    print (num)
    num = num - 1

print("Finished printing 1 to 5")
